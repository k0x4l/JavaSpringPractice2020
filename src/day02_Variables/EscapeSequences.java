package day02_Variables;

public class EscapeSequences {

    public static void main(String[] args) {
        System.out.println("\n My \nName \nis \nKoxal");
        System.out.println("\t My \t Name \t is \t Koxal");
        System.out.println("\"\"");
        System.out.println("\\");
        System.out.println("/");
        System.out.println("'");
        System.out.println("\'");


    }
}
